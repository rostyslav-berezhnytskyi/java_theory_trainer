/**
 * Self-evaluation result after checking the reference answer.
 *
 * AGAIN - I did not know the answer.
 * HARD  - I answered, but weakly.
 * GOOD  - I answered normally.
 * EASY  - I knew it confidently.
 */
public enum PracticeGrade {
    AGAIN,
    HARD,
    GOOD,
    EASY
}
